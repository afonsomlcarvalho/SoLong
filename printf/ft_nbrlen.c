#include "ft_printf.h"

int	nbrlen(long int nb)
{
	int	c;

	c = 1;
	if (nb < 0)
	{
		c++;
		nb = -nb;
	}
	while (nb > 9)
	{
		c++;
		nb = nb / 10;
	}
	return (c);
}
